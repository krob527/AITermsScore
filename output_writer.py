"""
output_writer.py – Persist the scorecard in Markdown, JSON, and HTML formats.
"""

from __future__ import annotations

import json
import re
from datetime import datetime, timezone
from pathlib import Path

from jinja2 import Environment, BaseLoader

from agent.runner import ScorecardResult

# ─── HTML template ────────────────────────────────────────────────────────────
