def _md_to_simple_html(md: str) -> str:
    """Minimal Markdown → HTML conversion (headers, bold, code blocks, links)."""
    try:
        import markdown as md_lib  # type: ignore
        return md_lib.markdown(md, extensions=["tables", "fenced_code"])
    except ImportError:
        # Fallback: wrap in <pre>
        escaped = md.replace("&", "&amp;").replace("<", "&lt;").replace(">", "&gt;")
        return f"<pre>{escaped}</pre>"


def _safe_filename(name: str) -> str:
    return re.sub(r"[^\w\-]", "_", name).lower()


def write_outputs(result: ScorecardResult, output_dir: Path) -> dict[str, Path]:
    """
    Write Markdown, JSON, and HTML scorecard files.
    Returns a dict of format → absolute Path.
    """
    ts = datetime.now(timezone.utc).strftime("%Y%m%dT%H%M%SZ")
    stem = f"{ts}_{_safe_filename(result.product_name)}"
    paths: dict[str, Path] = {}

    # ── Markdown ──────────────────────────────────────────────────────────────
    md_path = output_dir / f"{stem}.md"
    header = (
        f"# AI Terms Scorecard – {result.product_name}\n\n"
        f"**Vendor:** {result.vendor}  \n"
        f"**Generated:** {ts}  \n"
        f"**Run ID:** {result.run_id}  \n\n---\n\n"
    )
    md_path.write_text(header + result.raw_markdown, encoding="utf-8")
    paths["markdown"] = md_path

    # ── JSON ──────────────────────────────────────────────────────────────────
    json_path = output_dir / f"{stem}.json"
    structured = result.structured if isinstance(result.structured, dict) else {}
    payload = {
        "product_name": result.product_name,
        "vendor": result.vendor,
        "generated_utc": ts,
        "run_id": result.run_id,
        "thread_id": result.thread_id,
        "scores": structured,
        "raw_markdown": result.raw_markdown,
    }
    json_path.write_text(json.dumps(payload, indent=2, ensure_ascii=False), encoding="utf-8")
    paths["json"] = json_path

    # ── HTML ──────────────────────────────────────────────────────────────────
    html_path = output_dir / f"{stem}.html"
    env = Environment(loader=BaseLoader())  # type: ignore[call-arg]
    template = env.from_string(_HTML_TEMPLATE)
    # Ensure structured is always a dict for the Jinja2 template
    render_result = result
    if not isinstance(result.structured, dict):
        from dataclasses import replace
        render_result = replace(result, structured={})
    html_content = template.render(
        result=render_result,
        timestamp=ts,
        md_as_html=_md_to_simple_html(result.raw_markdown),
    )
    html_path.write_text(html_content, encoding="utf-8")
    paths["html"] = html_path

    return paths
